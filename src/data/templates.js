/**
 * Obtener templates por categoría
 * @param {string} categoria - Categoría de compra
 * @returns {Array} - Lista de templates
 */
export const getTemplatesByCategoria = (categoria) => {
  return TEMPLATES_ITEMS[categoria] || [];
};

/**
 * Obtener template por ID
 * @param {string} id - ID del template
 * @returns {Object|null} - Template encontrado
 */
export const getTemplateById = (id) => {
  for (const categoria in TEMPLATES_ITEMS) {
    const template = TEMPLATES_ITEMS[categoria].find(t => t.id === id);
    if (template) return template;
  }
  return null;
};

/**
 * Obtener todos los templates
 * @returns {Array} - Lista de todos los templates
 */
export const getAllTemplates = () => {
  const allTemplates = [];
  for (const categoria in TEMPLATES_ITEMS) {
    allTemplates.push(...TEMPLATES_ITEMS[categoria]);
  }
  return allTemplates;
};
